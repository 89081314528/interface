public class ArifmeticSum implements C{// сложный с базами данных
    int a;
    int b;

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public ArifmeticSum(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int summ() {
        return a + b;
    }

}
